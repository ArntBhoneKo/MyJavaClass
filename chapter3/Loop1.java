package chapter3;

public class Loop1 {

	public static void main(String[] args) {
		int i;
		for(i=1;i<10;i++)
		System.out.println("Hello");
		for(i=1;i<50;i++)
		System.out.print(i+" ");
		System.out.println("");
		for(i=2;i<100;i+=2)
		System.out.print(i+" ");
		System.out.println(" ");
		for(i=1;i<100;i+=2)
		System.out.print(i+" ");
		System.out.println(" ");
		for(i=100;i>2;i-=2)
		System.out.print(i+" ");
		System.out.println(" ");
		for(i=1;i<6;i++)
		System.out.print(i*i+" ");

	}

}
